    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DataBase db=new DataBase();
        boolean updated=db.UpdateMedicalState(jCheckBox1.isSelected(),jCheckBox2.isSelected(),jCheckBox3.isSelected(),jCheckBox4.isSelected(),jCheckBox5.isSelected(),Double.parseDouble(jTextField1.getText()));
        if (updated)
        {
            try {
                JOptionPane.showMessageDialog(this , "Donor Record has been Updated" , "Confirmation" , JOptionPane.INFORMATION_MESSAGE);
                String City= jLabel20.getText();
                String hospital= jLabel21.getText();
                UserPanel u=new UserPanel();
                u.jLabel20.setText(City);
                u.jLabel21.setText(hospital);
                //DataBase db=new DataBase();
                String name = db.Fetchname( City,  hospital);
                u.Name.setText(name);
                u.pack();
                u.setVisible(true);
                this.dispose();
                byte[] img = db.getimage(City,hospital);
                ImageIcon image = new ImageIcon(img);
                Image im = image.getImage();
                Image myImg = im.getScaledInstance(u.jLabel1.getWidth(), u.jLabel1.getHeight(),Image.SCALE_SMOOTH);
                ImageIcon newImage = new ImageIcon(myImg);
                u.jLabel1.setIcon(newImage);
                u.pack();
            } catch (InterruptedException ex) {
                Logger.getLogger(MedicalResults.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this , "Something Went Wrong" , "Error" , JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

