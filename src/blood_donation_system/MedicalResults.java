    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Double ID=(Double.parseDouble(jTextField1.getText()));
        DataBase db=new DataBase();
        String Name = db.DonorName(ID,jLabel20.getText() ,jLabel21.getText());
        if (Name == null)
        {
            JOptionPane.showMessageDialog(this , "ID isn't correct" , "Error" , JOptionPane.ERROR_MESSAGE);
            return;
        }
        else
        {
            jLabel1.setEnabled(true);
            jLabel30.setEnabled(true);
            jLabel30.setText(Name);
            jButton2.setEnabled(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

